print("按空格键显示一个假名（无重复）。按 q 键退出。\n")

def on_press(key):
    global kana_list
    try:
        if key.char == "q":
            print("已退出学习。")
            return False  # 停止监听
    except AttributeError:
        # 特殊键（如空格）
        if key == keyboard.Key.space:
            if kana_list:
                kana = kana_list.pop()
                print("👉", kana)
            else:
                print("\n所有假名都已经显示完了！")
                return False

with keyboard.Listener(on_press=on_press) as listener:
    listener.join()

